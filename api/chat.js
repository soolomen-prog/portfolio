function detectLanguage(text) {
  const t = text.toLowerCase();

  // русский
  if (/[а-яё]/i.test(t)) return "ru";

  // немецкий — по словам
  if (
    /\b(ich|und|sie|nicht|mit|für|eine|alles|liegt|ihrem|bitte|hallo)\b/i.test(t)
  ) {
    return "de";
  }

  // английский — по умолчанию
  return "en";
}

